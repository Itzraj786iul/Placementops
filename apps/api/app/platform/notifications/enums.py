    HIRING_OPPORTUNITY = "HIRING_OPPORTUNITY"
    APPLICATION = "APPLICATION"
    SHORTLIST_IMPORT = "SHORTLIST_IMPORT"
    STUDENT_PROFILE = "STUDENT_PROFILE"
